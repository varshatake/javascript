// Numeric string used with '-' '*' '/' result 

var result;

result = '4' - '2';
console.log(`Numeric string to number '4' - 2 =  ${result}`); 

result = '4' - 2;
console.log(`Numeric string to number '4' - 2=  ${result}`); 

result = '4' * 2;
console.log(`Numeric string to number '4' * 2=  ${result}`); 

result = '4' / 2;
console.log(`Numeric string to number '4' / 2=  ${result}`); 